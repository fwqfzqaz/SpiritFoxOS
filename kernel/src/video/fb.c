/* ---------- Serial debug helper ---------- */
static void fb_debug(const char *s)
{
    while (*s) {
        while (!(hal_inb(0x3FD) & 0x20))
            ;
        hal_outb(0x3F8, (uint8_t)*s);
        s++;
    }
}

